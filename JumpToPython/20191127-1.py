# 사칙연산

a = 3
b = 4
c = a * b
print('a * b : ' + str(c))
c = a / b
print('a / b : ' + str(c))

# 제곱을 나타내는 연산자 **
a = 3
b = 4
c = a ** b
print('a ** b : ' + str(c))

# 나머지를 반환하는 연산자 %
a = 7
b = 3
c = a % b
print('a % b : ' + str(c))

# 나눈 몫을 반환하는 연산자 //
a = 7
b = 3
c = a // b
print('a // b : ' + str(c))